package com.mytijian.admin.web.vo.hospital;

public class SiteVo {

	private int cssId;
	
	private int mobileTemplateId;
	
	private String url;

	public int getCssId() {
		return cssId;
	}

	public void setCssId(int cssId) {
		this.cssId = cssId;
	}

	public int getMobileTemplateId() {
		return mobileTemplateId;
	}

	public void setMobileTemplateId(int mobileTemplateId) {
		this.mobileTemplateId = mobileTemplateId;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}
	
}
